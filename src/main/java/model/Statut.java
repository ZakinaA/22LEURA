package model;

/**
 *
 * @author sio2
 */
public class Statut {
    
private int id;
private String libelle;

    public Statut() {
    }

    public Statut(int id, String libelle) {
        this.id = id;
        this.libelle = libelle;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLibelle() {
        return libelle;
    }

    public void setLibelle(String libelle) {
        this.libelle = libelle;
    }



}
